"""Listas en Python"""

"""Listas pop(): Quita un elemento de una posición dada"""

paises = ["Perú", "Canadá", "Argentina", "México"]

print("Mi lista inicial es la isguiente: {}".format(paises))

paises.pop()

print("Mi lista actualizada es la siguiente; {}".format(paises))

paises.pop(1)

print("Mi lista actualizada es la siguiente: {} ".format(paises))
