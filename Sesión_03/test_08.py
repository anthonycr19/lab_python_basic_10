"""Listas en Python"""

"""Listas (sort): Ordenar los elementos de una lista"""

var_1 = ["sQLServer", "rDS", "MYSQL", "Postgresql", "MariaDB"]
var_2 = [50, 2, 600, 150, 35, 40.5]

var_1.sort()
var_2.sort()

print("Mi primera lista ordenada es: {}".format(var_1))
print("Mi segunda lista ordenada es: {}".format(var_2))

